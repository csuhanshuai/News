package net.bangbao.dao;

import net.bangbao.base.BaseApi;
/**
 * BangBao登陆返回的数据
 * @author mosl
 *
 */
public class LoginUserApi extends BaseApi {
	
	private long info_tmtp;
	private long friend_tmtp;
	private long black_tmtp;
	private int update;
	private int user_id;
	private int type;
	private String token;
	private String rc_token;
	
	public long getInfo_tmtp() {
		return info_tmtp;
	}
	public void setInfo_tmtp(long info_tmtp) {
		this.info_tmtp = info_tmtp;
	}
	public long getFriend_tmtp() {
		return friend_tmtp;
	}
	public void setFriend_tmtp(long friend_tmtp) {
		this.friend_tmtp = friend_tmtp;
	}
	public long getBlack_tmtp() {
		return black_tmtp;
	}
	public void setBlack_tmtp(long black_tmtp) {
		this.black_tmtp = black_tmtp;
	}
	public int getUpdate() {
		return update;
	}
	public void setUpdate(int update) {
		this.update = update;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getRc_token() {
		return rc_token;
	}
	public void setRc_token(String rc_token) {
		this.rc_token = rc_token;
	}
	
}
